package sort.util;

import java.util.Arrays;
import java.util.Random;

import sort.base.SortTestable;

/**
 *
 * @author: xanxus
 *
 * @date: 2019年7月3日 下午1:37:48
 * 
 * @Description: 常用排序工具类
 *
 *
 */
public class CommonUtils {
	

	/** 
	 * @功能描述:	验证排序结果是否正确
	 *
	 * @参数说明:	a: 待验证数组
	 *          
	 * @返回值:	boolean类型: true 数组排序正确; false 排序错误 
	 *
	 * @作者:	xanxus
	 *
	 * @创建时间:	2019年7月3日 
	 */  
	public static int validateResults(int[] a){
		for(int i = 0; i < a.length - 1; i++)
			if(a[i] > a[i + 1])return i;
		return -1;
	}
	
  
 
	/** 
	 * @功能描述:	创建随机数组(元素在[0, max)之间)
	 *
	 * @参数说明:	length:	需要创建的数组长度
	 * 
	 * @参数说明:	max:	数组元素的最大值(不包括该值)
	 * 
	 * @返回值:	int[]
	 *
	 * @作者:	xanxus
	 *
	 * @创建时间:	2019年7月3日 
	 */  
	public static int[] createIntArrays(int length, int max){
		if(max <= 0)throw new IllegalArgumentException("max的值应大于0. " + max);
		int[] a = new int[length];
		Random random = new Random(System.currentTimeMillis());
		for(int i = 0; i < a.length; i++){
			a[i] = random.nextInt(max);
		}
		return a;
	}
	
	/** 
	 * @功能描述:	交换数组中任意两个元素的值
	 *
	 * @参数说明:	a
	 * @参数说明:	i
	 * @参数说明:	j
	 *          
	 * @作者:	 xanxus
	 *
	 * @创建时间:	2019年7月3日 
	 */  
	public static void swap(int[] a, int i, int j){
		int temp = a[i];
		a[i] = a[j];
		a[j] = temp;
	}
	
	/** 
	 * @功能描述:	复制数组
	 *
	 * @参数说明:	src
	 * @参数说明:	@return
	 *          
	 * @返回值:	int[]
	 *
	 * @作者:	xanxus
	 *
	 * @创建时间:	2019年7月4日 
	 */  
	public static int[] copyArrays(int[] src){
		int[] dest = new int[src.length];
		System.arraycopy(src, 0, dest, 0, src.length);
		return dest;
	}
	
	
	/** 
	 * @功能描述:	测试排序
	 *
	 * @参数说明:	name
	 * @参数说明:	a
	 * @参数说明:	testSort
	 *          
	 * @返回值:	void
	 *
	 * @作者:	xanxus
	 *
	 * @创建时间:	2019年7月4日 
	 */  
	public static void doTestSort(String name , int[] a,SortTestable testSort){
		doTestSort(name, a, testSort, false);
	}
	
	/** 
	 * @功能描述:	测试排序结果
	 *
	 * @参数说明: name: 排序算法名字
	 * @参数说明: a: 待排序数组
	 * @参数说明:	testSort: 排序算法
	 * @参数说明:	isShowResults: 是否显示排序结果
	 *          
	 * @返回值:	void
	 *
	 * @作者:	xanxus
	 *
	 * @创建时间:	2019年7月4日 
	 */  
	public static void doTestSort(String name , int[] a,SortTestable testSort, boolean isShowResults){
		System.out.println(name + "排序算法开始排序:......");
		long startTime1 = System.currentTimeMillis();
		testSort.test(a);
		long endTime1 = System.currentTimeMillis();
		int isSorted1 = CommonUtils.validateResults(a);
		System.out.println(name + "算法排序是否正确: " + (isSorted1 > -1 ? "排序错误." + isSorted1 : "排序正确." ));
		if(isShowResults)System.out.println(name + "算法排序结果: " + Arrays.toString(a));
		System.out.println(name + "算法排序用时："  + (endTime1 - startTime1));
		System.out.println("=================================================================");
	}
}
